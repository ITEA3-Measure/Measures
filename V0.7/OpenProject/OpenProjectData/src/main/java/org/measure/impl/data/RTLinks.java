package org.measure.impl.data;

import org.measure.impl.data.RTLink;

public class RTLinks {

    private RTLink self;

    private RTLink project;

    private RTLink version;

    private RTLink parent;

    private RTLink author;

    private RTLink responsible;

    private RTLink assignee;

    private RTLink priority;

    private RTLink status;

    public RTLink getSelf() {
        
        return self;
    }

    public void setSelf(RTLink self) {
        this.self = self;
    }

    public RTLink getProject() {
        
        return project;
    }

    public void setProject(RTLink project) {
        this.project = project;
    }

    public RTLink getVersion() {
        
        return version;
    }

    public void setVersion(RTLink version) {
        this.version = version;
    }

    public RTLink getParent() {
        
        return parent;
    }

    public void setParent(RTLink parent) {
        this.parent = parent;
    }

    public RTLink getAuthor() {
        
        return author;
    }

    public void setAuthor(RTLink author) {
        this.author = author;
    }

    public RTLink getResponsible() {
        
        return responsible;
    }

    public void setResponsible(RTLink responsible) {
        this.responsible = responsible;
    }

    public RTLink getAssignee() {
        
        return assignee;
    }

    public void setAssignee(RTLink assignee) {
        this.assignee = assignee;
    }

    public RTLink getPriority() {
        
        return priority;
    }

    public void setPriority(RTLink priority) {
        this.priority = priority;
    }

    public RTLink getStatus() {
        
        return status;
    }

    public void setStatus(RTLink status) {
        this.status = status;
    }

}
